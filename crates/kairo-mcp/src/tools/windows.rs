//! # Windows tools (`mcp__kairo__windows`)
//!
//! - `windows_list_apps` — list all open windows with titles and PIDs
//! - `windows_focus_app` — bring a window to foreground
//! - `windows_launch` — launch an application
//! - `windows_close_app` — close an application
//! - `windows_ui_click` — click a UI element by accessibility reference
//! - `windows_ui_type` — type text into the active window
//! - `windows_clipboard_get` — read clipboard
//! - `windows_clipboard_set` — write clipboard
//! - `windows_notification` — post a system notification
